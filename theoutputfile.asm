;basic stuff from the tutorial
SECTION "start",HOME[$0100]
    nop
    jp lets_go
    ROM_HEADER  ROM_NOMBC, ROM_SIZE_32KBYTE, RAM_SIZE_0KBYTE

lets_go:
    nop 
    di 
    ld sp, $ffff

initialization:
    ld a, %11100100
    ld [rBGP], a ;four colors
    ld a, 0
    ld [rSCX], a ;X=0
    ld [rSCY], a ;Y=0
    call turn_off_LCD ;yeah for now we don't wanna damage the gameboy

    ;let's load 4096 bytes into vram for now
    ld hl, TileArray
    ld de, _VRAM
    ld bc, 4096

.loading_stuff:
    ld a, [hl]
    ld [de], a
    dec bc
    ld a, b
    ;gotta get rid of the nintendo logo
    or c
    jr z, .check_if_over
    inc hl
    inc de
    jr .loading_stuff

;clean it all up
.check_if_over:
    ld hl, _SCRN0
    ld de, 32*32

;OKAY LETS PRINT THE IMG
.loop_handler
    ld a, 0
    ld b, 14 ;Let's print 14 tiles only on the first line
    ld c, 13

.print_hai
    ld hl, _SCRN0+32*2+9
    ld [hl], 254
    inc hl
    ld [hl], 255
    ;and let's move the cursor to the actual image printing position
    ld hl, _SCRN0+32*5+6

;print the whole row
.print_row
    ld [hl], a
    inc hl
    inc a
    dec b
    jr z, .horizontal_row_next
    jr .print_row

;skip the tiles you can't see
.horizontal_row_next
    ld b, 20
    ld de, 12
    add hl, de
    dec c
    jr z, .done
    jr .print_row

;and we're done
.done
    ld a, LCDCF_ON|LCDCF_BG8000|LCDCF_BG9800|LCDCF_BGON|LCDCF_OBJ8|LCDCF_OBJOFF
    ld [rLCDC], a

infinite_loop:
    halt
    nop 
    jr infinite_loop

turn_off_LCD:
    ld a, [rLCDC]
    rlca 
    ret nc

.close_VBlank
    ld a,[rLY]
    cp 145
    jr nz, .close_VBlank
    ld a,[rLCDC]
    res 7,a
    ld [rLCDC], a
    ret

